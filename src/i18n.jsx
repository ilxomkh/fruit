const LocaleContext = createContext();

export function LocaleProvider({ children }) {
  // Загружаем сохраненный язык из localStorage или используем 'ru' по умолчанию
  const [locale, setLocale] = useState(() => {
    if (typeof window !== 'undefined') {
      const savedLocale = localStorage.getItem('fruitexim-locale');
      return savedLocale && (savedLocale === 'ru' || savedLocale === 'uz') ? savedLocale : 'ru';
    }
    return 'ru';
  });

  // Сохраняем язык в localStorage при изменении
  useEffect(() => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('fruitexim-locale', locale);
    }
  }, [locale]);

  const value = useMemo(() => {
    const pack = locales[locale];
    return {
      locale,
      setLocale,
      t: pack,
    };
  }, [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used inside LocaleProvider');
  }
  return context;
}

